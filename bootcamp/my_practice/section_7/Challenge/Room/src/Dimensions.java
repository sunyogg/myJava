public class Dimensions {

    private int length;
    private int height;
    private int breadth;

    public Dimensions(int length, int height, int breadth) {
        this.length = length;
        this.height = height;
        this.breadth = breadth;
    }

    public int getLength() {
        return length;
    }

    public int getHeight() {
        return height;
    }

    public int getBreadth() {
        return breadth;
    }
}
